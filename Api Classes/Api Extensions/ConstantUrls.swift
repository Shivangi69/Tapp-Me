class ConstantUrls: NSObject {

    class func removeNullValue(dict : NSDictionary) -> NSDictionary {
        let newDict = NSMutableDictionary.init()
        for key in dict.allKeys {
            dict.object(forKey: key) is NSNull ? newDict.setValue("", forKey: key as! String) : newDict.setValue(dict.object(forKey: key), forKey: key as! String)
        }
        return newDict
    }

    
    class func validateEmail(emailAddress : String) -> Bool{
        
        do {
            let regExPattern = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,4}$"
            let regEx = try  NSRegularExpression(pattern: regExPattern, options: .caseInsensitive)
            
            let regExMatches = regEx.numberOfMatches(in: emailAddress, options: NSRegularExpression.MatchingOptions(rawValue: 0), range: NSRange(location: 0, length:emailAddress.count))
            return regExMatches == 0 ? false : true

        } catch {
            return false
        }
    }

}
